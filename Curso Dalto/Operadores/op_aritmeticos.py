#Operadores aritmeticos

a = 10
b = 5

#Suma y Resta (+ y -)

suma = a + b
resta = b - a

#Multiplicacion y Division (* y /)

multi = a * b
divi = a / b #nos devuuelve un dato de tipo float

#Potencia (**)

exp = a ** b

#Division baja (//) 

divi_baja = a // b #nos devueve un entero (redondeado hacia abajo)

#Resto o Modulo (%)

resto = a % b

print (suma)
print (resta)
print (multi) 
print (divi)
print (exp)
print (divi_baja)
print (resto)

##########################################################

#type() -> nos permite saber el tipo de dato

tipo_de_dato = type(divi)

